package application.healthSoftware.data;

public class Prescription {
	public String name;
	public String dosage;
	public String duration;
	
	public Prescription() {
		
	}
	
	public String getName() {
		return name;
	}
	
	public String getDosage() {
		return dosage;
	}
	
	public String getDuration() {
		return duration;
	}
}
